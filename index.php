		function validLogin(){
			var exampleInputAmount=$('#exampleInputAmount').val();
			var dataString = 'mobile='+ exampleInputAmount ;
			$.ajax({
			      type: "POST",
			      url: "//staging.marketinsights.in/public/index.php/sendCustomMessage",
			      data: dataString,
			      cache: false,
			      beforeSend: function () { $('body').addClass("loading"); },
					success: function(result){
						$('#exampleInputAmount').val("");
				      $('body').removeClass("loading");
				       var result=trim(result);
				       if(result=='Success'){
				            ShowAlertMessage("We have sent link to download the app.",'info',window.SuccessConfirm);
				       }else{
				            ShowAlertMessage(result,'error',window.ConfirmDialogSomethingWrong);
				       }
				}
			});
